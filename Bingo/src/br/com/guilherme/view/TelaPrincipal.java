    public static List<Integer> obterNumerosDaCartela(){
        List<Integer> nums = new ArrayList<>();
        Stack pilha = new Stack();
        for(int i = 0; i < 60; i++){
            pilha.push(i+1);
        }
        for(int i = 0; i< 12; i++){
            Collections.shuffle(pilha);
            int numero = (int) pilha.pop();
            nums.add(numero);
        }
        return nums;
    }
    
    public TelaPrincipal(int numeroDeCartelas){
        this();
        this.numeroDeCartelas = numeroDeCartelas;
        this.setVisible(true);
        this.listaDeCartelas.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        this.pilhaNumerosSorteio = new Stack();
        this.cartelas = new ArrayList<>();
        this.qtdeNumSorteados = 0;
        this.oneWinner = false;
        this.pilhaNumerosSorteados = new Stack();
        
        for(int i = 0; i < 60; i++){
            this.pilhaNumerosSorteio.push(i+1);
        }
        
        for(int i = 0; i < this.numeroDeCartelas; i++){
            Cartela novaCartela = new Cartela(this.obterNumerosDaCartela());
            this.cartelas.add(novaCartela);
        }
       
        this.listaDeCartelas.setListData(cartelas.toArray());
        this.listaDeCartelas.setSelectedIndex(0);
        
        jScrollPane2.setHorizontalScrollBarPolicy(ScrollPaneConstants.HORIZONTAL_SCROLLBAR_NEVER);
        txtAreaNumSorteados.setLineWrap(true);
        txtAreaNumSorteados.setWrapStyleWord(true);
    }

    
