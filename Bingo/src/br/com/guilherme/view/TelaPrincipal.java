    private void btnSortearProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortearProximoActionPerformed
        try{
            if(pilhaNumerosSorteio.size() > 0){
                Collections.shuffle(pilhaNumerosSorteio);
                int numeroSorteado = (int) this.pilhaNumerosSorteio.pop();
                this.pilhaNumerosSorteados.push(numeroSorteado);
                txtAreaNumSorteados.append("["+numeroSorteado+"]" + " ");
                for(Cartela c : cartelas){
                    List<Integer> valores = c.getValores();
                    for (Integer valor : valores) {
                        if(valor == numeroSorteado){
                            c.addPonto();
                        }
                    }
                    
                    if(c.getPontos() == 12 && c.getVenceu()==false){
                        cbCartelasVencedoras.addItem("Cartela " + c.getNumCartela());
                        listaDeCartelas.setSelectedIndex(c.getNumCartela()-1);
                        listaDeCartelas.setSelectionBackground(Color.RED);
                        c.setVenceu(true);
                        if(!oneWinner){
                            JOptionPane.showMessageDialog(null, "VENCEDOR ENCONTRADO!!!");
                            oneWinner=true;
                        }
                    }
                }
                this.qtdeNumSorteados++;
                lblQtdeNumSorteados.setText("Sorteados: " + this.qtdeNumSorteados);
            }else{
                JOptionPane.showMessageDialog(null, "Fim do sorteio, os números acabaram!");
            }
        }catch(Exception erro){
            JOptionPane.showMessageDialog(null, "Erro, tente novamente!");
        }
    }//GEN-LAST:event_btnSortearProximoActionPerformed

    private void btnSortearTudoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortearTudoActionPerformed
        try{
            for(int i = 0; i < (60 - this.qtdeNumSorteados); i++){
                btnSortearProximo.doClick();
            }
        }catch(Exception erro){
            JOptionPane.showMessageDialog(null, "Não foi possível realizar o sorteio.");
        }
    }//GEN-LAST:event_btnSortearTudoActionPerformed

